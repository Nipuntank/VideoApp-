  Future<void> _login() async {
    final storage = FlutterSecureStorage();
    final String? storedToken = await storage.read(key: 'token');

    if (storedToken != null) {
      // Send the token to the backend for validation
      final http.Response tokenValidationResponse = await http
          .post(
            Uri.parse('http://${GlobalState.apiIP}:8282/login'),
            headers: <String, String>{
              'Content-Type': 'application/json; charset=UTF-8',
            },
            body: jsonEncode({'token': storedToken}),
          )
          .timeout(const Duration(seconds: 30));

      if (tokenValidationResponse.statusCode == 200) {
        // Token is valid, navigate to the home page
        context.go('/homePage');
        return;
      } else {
        // Token is invalid or expired, remove it from storage
        await storage.delete(key: 'token');
      }
    }

    // Proceed with the normal login process if no valid token is found
    final String username = _model.textController1?.text ?? '';
    final String password = _model.textController2?.text ?? '';

    // Hash the password using SHA256
    final String hashedPassword =
        sha256.convert(utf8.encode(password)).toString();

    // Prepare the request data
    final Map<String, dynamic> data = {
      'username': username,
      'password': hashedPassword,
    };

    // Send the POST request
    final http.Response response = await http
        .post(
          Uri.parse('http://${GlobalState.apiIP}:8282/login'),
          headers: <String, String>{
            'Content-Type': 'application/json; charset=UTF-8',
          },
          body: jsonEncode(data),
        )
        .timeout(const Duration(seconds: 30));

    // Handle the response
    if (response.statusCode == 200) {
      // Parse the token from the response
      final Map<String, dynamic> responseData = jsonDecode(response.body);
      final String token = responseData['token'];

      // Store the token securely using the flutter_secure_storage package
      final storage = FlutterSecureStorage();
      await storage.write(key: 'token', value: token);

      // To use the token in future API requests:
      // final storedToken = await storage.read(key: 'token');
      // Include 'Authorization' header with the token value in the request
      // Navigate to the home page
      // Navigator.pushReplacementNamed(context, 'HomePage');
      context.go('/homePage');
    } else {
      // Show an error message
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Invalid username or password.')),
      );
    }
  }

